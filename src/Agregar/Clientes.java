    private void jM_GuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jM_GuardarMouseClicked
        try {
            Connection conexion = DBConnection.getConnection();
            int max = DBConnection.ClienteMax();

            if (("".equals(jT_nombre.getText())) || (" ".equals(jT_nombre.getText())) || (jT_nombre.getText() == null) || ("".equals(jT_Ruc.getText())) || (" ".equals(jT_Ruc.getText())) || (jT_Ruc.getText() == null)) {
                JOptionPane.showMessageDialog(this, "Completar Nombre / RUC");
            } else {

                if (id_cliente == 0) {
                    PreparedStatement st;
                    st = conexion.prepareStatement("INSERT INTO cliente VALUES(?,?,?,?,?,?,?)");
                    st.setInt(1, (max + 1));
                    st.setString(2, jT_nombre.getText());
                    st.setString(3, jT_Direccion.getText());
                    st.setString(4, jT_telefono.getText());
                    st.setString(5, jT_observaciones.getText());
                    st.setString(6, jT_Ruc.getText());
                    st.setInt(7, 0);
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Cliente agregado correctamente");
                } else {

                    PreparedStatement st = conexion.prepareStatement("UPDATE cliente SET nombre ='" + jT_nombre.getText() + "', direccion ='" + jT_Direccion.getText() + "', telefono ='" + jT_telefono.getText() + "', ruc ='" + jT_Ruc.getText() + "' WHERE id_cliente = '" + id_cliente + "'");
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Cliente actualizado correctamente");
                }

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }


    }//GEN-LAST:event_jM_GuardarMouseClicked

