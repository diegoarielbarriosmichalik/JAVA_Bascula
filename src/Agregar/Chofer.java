    private void jMenu2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu2MouseClicked
        try {
            Connection conexion = DBConnection.getConnection();
            int max = DBConnection.ChoferMax();

            if (("".equals(jT_Nombre.getText())) || (" ".equals(jT_Nombre.getText())) || (jT_Nombre.getText() == null) || ("".equals(jT_CI.getText())) || (" ".equals(jT_CI.getText())) || (jT_CI.getText() == null)) {
                JOptionPane.showMessageDialog(this, "Completar Nombre / CI");
            } else {

                if (id_chofer == 0) {
                    PreparedStatement st;
                    st = conexion.prepareStatement("INSERT INTO chofer VALUES(?,?,?,?,?,?)");
                    st.setInt(1, (max + 1));
                    st.setString(2, jT_Nombre.getText());
                    st.setString(3, jT_CI.getText());
                    st.setString(4, jT_telefono.getText());
                    st.setString(5, jT_Direccion.getText());
                    st.setInt(6, 0);
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Chofer agregado correctamente");
                } else {

                    PreparedStatement st = conexion.prepareStatement("UPDATE chofer SET nombre ='" + jT_Nombre.getText() + "', direccion ='" + jT_Direccion.getText() + "', telefono ='" + jT_telefono.getText() + "', ci ='" + jT_CI.getText() + "' WHERE id_chofer = '" + id_chofer + "'");
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Chofer actualizado correctamente");
                }

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }//GEN-LAST:event_jMenu2MouseClicked

